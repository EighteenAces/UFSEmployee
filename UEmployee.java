public class UEmployee {

   private String memberName;
   private double memberSalary;

    /**
     * @return String return the memberName
     */
    public String getMemberName() {
        return memberName;
    }

    /**
     * @param memberName the memberName to set
     */
    public void setMemberName(String memberName) {
        this.memberName = memberName;
    }

    /**
     * @return double return the memberSalary
     */
    public double getMemberSalary() {
        return memberSalary;
    }

    /**
     * @param memberSalary the memberSalary to set
     */
    public void setMemberSalary(double memberSalary) {
        this.memberSalary = memberSalary;
    }

}
